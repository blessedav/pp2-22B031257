x = 5
print(type(x))
#it will print "int"

x = "Hello world"
print(type(x))
#it will print "str"

x = 20.5
print(type(x))
#it will print "float"

x = ["apple", "banana" , "cherry"]
print(type(x))
#it will print "list"

x = ("apple", "banana", "cherry")
print(type(x))
#it will print "tuple"

x = {"name" : "John", "age" : 36}
print(type(x))
#it will print "dict"

x = True
print(type(x))
#it will print "bool"